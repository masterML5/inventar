    public ToneriIzdavanje(String username, String toner, int idToner, int kolicina, int prep_kolicina, String datum) throws SQLException {
        initComponents();
         try {
            conSQL = DriverManager.getConnection(connectionUrlMySQL);
            conSQL.setAutoCommit(false);
        } catch (SQLException ex) {
            System.out.println(ex);

        }
        tp = new ToneriPregled();
        ArrayList<String> sveLokacije = lok.getAll();
        sveLokacije.add(0, null);
        lokacijaComboBox.setModel(new DefaultComboBoxModel<>(sveLokacije.toArray(new String[0])));
        AutoCompleteDecorator.decorate(lokacijaComboBox);
        tonerLabel.setText(toner);
        
        if(kolicina < prep_kolicina){
                kolLabel.setText("<html><p color='red'><b>"+kolicina+"</b></p></html>");
            }else if(kolicina == prep_kolicina){
                kolLabel.setText("<html><p color='FF8C00'><b>"+kolicina+"</b></p></html>");
            }else{
                kolLabel.setText("<html><p color='green'><b>"+kolicina+"</b></p></html>");
            }
        
        kolicinaSpin.setMaximum(kolicina);
        kolicinaSpin.setMinimum(1);
        kolicinaSpin.setValue(1);
        nazivToner = toner;
        this.datum = datum;
        this.username = username;
        this.idToner = idToner;
    }
    boolean updateTonerKol (int idToner, int kol) throws SQLException{
        int kolicinaS = tp.getKolicinaById(idToner);
        int kolicina = kolicinaS - kol;
        String sqlTonerKol = "UPDATE toneri set kolicina =" + kolicina +" WHERE id_toner="+idToner;
        PreparedStatement pstUpdateKol = conSQL.prepareStatement(sqlTonerKol);
        int i = pstUpdateKol.executeUpdate();
        conSQL.commit();
        return i > 0;
    }

